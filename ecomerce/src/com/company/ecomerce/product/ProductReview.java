package com.company.ecomerce.product;

public class ProductReview {
	private String review;
	private int rating;
	ProductReview(){}
	public String getReview() {
		return review;
	}
	public void setReview(String review) {
		this.review = review;
	}
	public int getRating() {
		return rating;
	}
	public void setRating(int rating) {
		this.rating = rating;
	}
}
